## This file contains all the commands that were done throughout the development of this app

## mix phx.new message_app

## create commands_list.ex        ../message_app/commands_list.ex

## run `mix phx.gen.context Accounts User users username:string` to create the Account context and User schema

## run `mix ecto.create` to create a database for the app

## run `mix ecto.migrate` to run migration

## create `user_controller.ex` file in ../message_app/lib/message_app_web/controllers/user_controller.ex

## create `user` subdirectory in ../message_app/lib/message_app_web/templates/user

## create `new.html.eex` in ../message_app/lib/message_app_web/templates/user/new.html.eex

## create `user_view.ex` file in ../message_app/lib/message_app_web/views/user_view.ex

## run `mix phx.gen.context Messages Message messages  message:string` to create the Messages context and Message schema

## create `message_controller.ex` file in ../message_app/lib/message_app_web/controllers/message_controller.ex

## create `message` subdirectory in ../message_app/lib/message_app_web/templates/message

## create `new.html.eex` in ../message_app/lib/message_app_web/templates/message/new.html.eex

## create `message_view.ex` file in ../message_app/lib/message_app_web/views/message_view.ex

## run `mix deps.get` after :arc and :arc_ecto have been added as dependencies

## create `attach_file.ex` in ../message_app/lib/messages/attach_file.ex

## create `index.html.eex` in ../message_app/lib/message_app_web/templates/message/index.html.eex









